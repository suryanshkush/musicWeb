import java.util.*;
public class pattern {
    public static void main(String args[]){
        int row=5;
        int ch=1;
        for (int i=1;i<=row;i++){
            for(int j=1;j<=i;j++){
                    System.out.print(ch);
                    ch++;     
                }
                System.out.println();
            }
            
        }
    }
